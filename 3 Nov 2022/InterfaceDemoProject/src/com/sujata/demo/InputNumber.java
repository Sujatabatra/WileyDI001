package com.sujata.demo;

public class InputNumber {

	private int value;
	
	public InputNumber() {
		
	}

	public InputNumber(int value) {
		super();
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
}
